int		ft_isalnum(char *str)
{
	int i;

	i = 0;
	while ((str[i] <= 'z' && str[i] >= 'a' && str[i]) || (str[i] <= 'Z' &&
			str[i] >= 'A' && str[i]) || (str[i] <= '9' && str[i] >= '0'))
		i++;
	if (str[i] == '\0')
		return (1);
	return (0);
}
