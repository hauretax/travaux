#include "libft.h"

char	*ft_itoa(int n)
{
	int		i;
	char	*sr;

	i = ft_sizint(n);
	if (n < 0)
		i++;
	if ((sr = malloc(sizeof(char) * i + 1)) == 0)
		return (NULL);
	sr[i] = '\0';
	if (n < 0)
		sr[0] = '-';
	i--;
	while (i > 0)
	{
		sr[i] = (ABS((n % 10)) + '0');
		i--;
		n = n / 10;
	}
	if (sr[0] == '\0')
		sr[i] = (ABS((n % 10)) + '0');
	return (sr);
}
