int		ft_atoi(const char *str)
{
	int nb;
	int i;
	int n;

	i = 0;
	n = 1;
	nb = 0;
	while (str[i] < ' ' && str[i])
		i++;
	if (str[i] == '-')
		n = -1;
	if (str[i] == '-')
		i++;
	else if (str[i] == '+')
		i++;
	while (str[i] <= '9' && str[i] >= '0' && str[i])
	{
		nb = nb + str[i] - '0';
		i++;
		if (str[i] <= '9' && str[i] >= '0' && str[i])
			nb = nb * 10;
	}
	return (nb * n);
}
