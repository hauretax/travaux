#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void *v;
	char *s;

	if ((s = malloc(size)) == 0)
		return (0);
	v = (void *)s;
	while (size--)
	{
		*s = 0;
		s++;
	}
	return (v);
}
