#include "libft.h"

char	*ft_alocaftchr(char *s, int c)
{
	size_t	i;
	
	i = 0;
	while (s[i] != c && s[i] != '\0')
		i++;
	return (ft_strsub(s, i, ft_strlen(s)));
}
